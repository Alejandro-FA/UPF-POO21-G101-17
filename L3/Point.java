public class Point {
    /************************ Attributes *************************/
    private double x;
    private double y;

    /************************ Constructor ************************/
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    /************************ Methods ****************************/
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
