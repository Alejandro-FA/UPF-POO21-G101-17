public class Point {
    /************************ Attributes *************************/
    protected double x;
    protected double y;

    /************************ Constructor ************************/
    public Point(double xInit, double yInit) {
        x = xInit;
        y = yInit;
    }

    /************************ Methods ****************************/
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
